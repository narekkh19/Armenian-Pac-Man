void Map::draw(sf::RenderWindow& window) {
    for (size_t i = 0; i < level.size(); ++i) {
        for (size_t j = 0; j < level[i].size(); ++j) {
            if (level[i][j] == 2) { //Block
                tuf.drawTuF(window, i, j);
            }
            else if (level[i][j] == 3) { //Food
                hazarnoc.drawDram(window, i, j);
            }
        }
    }
    
}